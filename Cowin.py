import requests
import datetime
import time
def getDate():
  current_time = datetime.datetime.now()
  day =  str(current_time.day)
  month = str(current_time.month)
  year = str(current_time.year)
  if int(day)<10:
      day = '0' + day
  if int(month)<10:
      month = '0' + month

  date = day + "-"+month + "-" + year
  return date


def available():
  date = getDate()
  districtId = '395'
  Url = "https://cdn-api.co-vin.in/api/v2/appointment/sessions/public/findByDistrict?district_id={}&date={}".format(districtId,date)
  header = {
      'User-Agent': 'Mozilla/5.0 (Windows NT 6.1; WOW64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/56.0.2924.76 Safari/537.36'}
  response = requests.get(Url,header).json()
  data = response["sessions"]
  age = 18
  counter = 0
  for each in data:
    if(each["available_capacity"]>0 and each["min_age_limit"] == age): 
      if((each["available_capacity"] > 0) & (each["min_age_limit"] == 18)):
              counter += 1
              print(each["name"])
              print(each["pincode"])
              print(each["vaccine"])
              print(each["available_capacity"])
              return True
      if(counter == 0):
          print("No Available Slots")
          return False

while(available()!=True):
  time.sleep(5)
  available()

